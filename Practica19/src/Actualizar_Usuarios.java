    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // Mostrar cuadro de confirmación
    int opcion = JOptionPane.showConfirmDialog(this,"¿Estás seguro de que quieres guardar los cambios?","Confirmar Cambios",JOptionPane.YES_NO_OPTION);

    if (opcion == JOptionPane.YES_OPTION) {
        try {
            // Obtener datos del formulario
            int id = Integer.parseInt(txtId.getText());
            String Nombre = txtNombre.getText();
            String Correo = txtCorreo.getText();
            String Contrasena = txtContrasena.getText();
          
            // Actualizar en la base de datos
            Connection conn = ConexionMySQL.conectar();
            String sql = "UPDATE usuarios SET Nombre=?, Correo=?, Contrasena=? WHERE id=?";
            PreparedStatement pstmt = conn.prepareStatement(sql);

            pstmt.setString(1, Nombre);
            pstmt.setString(2, Correo);
            pstmt.setString(3, Contrasena);
            pstmt.setInt(4, id);

            pstmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "Cambios guardados correctamente.");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error: " + ex.getMessage());
        }
    } else {
        JOptionPane.showMessageDialog(null, "Los cambios no fueron guardados.");
    }
    }//GEN-LAST:event_btnEditarActionPerformed

