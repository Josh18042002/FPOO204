    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String idtext= txtId.getText();
        
        if(idtext.isEmpty()){
            JOptionPane.showMessageDialog(this,"El id es obligatorio ","Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        try {
    int id = Integer.parseInt(txtId.getText()); // Obtener el ID del campo de texto
    ResultSet rs = crud.buscarporId(id); // Método que realiza la consulta SQL

    if (rs.next()) { 
        // Si se encuentra el usuario, se llenan los campos con los datos obtenidos
        txtNombre.setText(rs.getString("Nombre"));
        txtCorreo.setText(rs.getString("Correo"));
        txtContrasena.setText(rs.getString("Contrasena"));
    } else {
        // Si no se encuentra el usuario, se muestran mensajes y se limpian los campos
        JOptionPane.showMessageDialog(this, "Usuario no encontrado", "Error", JOptionPane.ERROR_MESSAGE);
        txtNombre.setText("");
        txtCorreo.setText("");
        txtContrasena.setText("");
    }
  } catch (SQLException e) {
    JOptionPane.showMessageDialog(this, "Error al buscar usuario: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
}
        
        
    }//GEN-LAST:event_btnBuscarActionPerformed

